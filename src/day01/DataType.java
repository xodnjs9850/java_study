package day01;

public class DataType {
    public static void main(String[] args) {
        int a = 50;
        short b = 32767;
        int c = 2147483647;
        long d = 2147483648L;
        System.out.println("d = " + d);
        float f1 = 3.71252365736262626F;
        double d1 = 3.7325740346535353535353539;
        System.out.println(f1);
        System.out.println(d1);
    }
}
